package com.js.oa.dcq.util;

public class MeetingAccdoc {
  private String oid;
  
  private String meetingName;
  
  private String beginTime;
  
  private String endTime;
  
  private String meetingSite;
  
  public String getOid() {
    return this.oid;
  }
  
  public void setOid(String oid) {
    this.oid = oid;
  }
  
  public String getMeetingName() {
    return this.meetingName;
  }
  
  public void setMeetingName(String meetingName) {
    this.meetingName = meetingName;
  }
  
  public String getBeginTime() {
    return this.beginTime;
  }
  
  public void setBeginTime(String beginTime) {
    this.beginTime = beginTime;
  }
  
  public String getEndTime() {
    return this.endTime;
  }
  
  public void setEndTime(String endTime) {
    this.endTime = endTime;
  }
  
  public String getMeetingSite() {
    return this.meetingSite;
  }
  
  public void setMeetingSite(String meetingSite) {
    this.meetingSite = meetingSite;
  }
}
