package com.js.oa.webservice.tjqz;

public class OrgPO {
  private String orgGuid;
  
  private String orgParentId;
  
  private String orgName;
  
  private String orgCode;
  
  private String orgLeader;
  
  private String orgDescripte;
  
  public String getOrgGuid() {
    return this.orgGuid;
  }
  
  public void setOrgGuid(String orgGuid) {
    this.orgGuid = orgGuid;
  }
  
  public String getOrgParentId() {
    return this.orgParentId;
  }
  
  public void setOrgParentId(String orgParentId) {
    this.orgParentId = orgParentId;
  }
  
  public String getOrgName() {
    return this.orgName;
  }
  
  public void setOrgName(String orgName) {
    this.orgName = orgName;
  }
  
  public String getOrgCode() {
    return this.orgCode;
  }
  
  public void setOrgCode(String orgCode) {
    this.orgCode = orgCode;
  }
  
  public String getOrgLeader() {
    return this.orgLeader;
  }
  
  public void setOrgLeader(String orgLeader) {
    this.orgLeader = orgLeader;
  }
  
  public String getOrgDescripte() {
    return this.orgDescripte;
  }
  
  public void setOrgDescripte(String orgDescripte) {
    this.orgDescripte = orgDescripte;
  }
}
